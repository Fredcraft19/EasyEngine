class ProjectSetup{
    // project settings
    target_fps = 60;
    fullscreen = true;
    display_size = null;

    // project storage/setup
    gameobjects = [];
}

class Builder{
    static projectState = null;

    static build_directory = null;

    static start_time = null;
 
    static project_name = "default_name";
 
    static setup_json = "";
    static cc_js = "";
    static init = "";

    static async SaveProject(){
        console.log("saving project..")
        this.projectState = new ProjectSetup();

        this.projectState.target_fps = 60;
        this.projectState.fullscreen = true;
        this.projectState.display_size = {x: Engine.DisplaySize.x, y: Engine.DisplaySize.y};

        Engine.GameObjects.forEach(obj => {
            let data = obj.Serialize();
            obj.components.forEach(c => {
                data.components.push(c.Serialize());
            });
            this.projectState.gameobjects.push(data);
        });

        // Done
        console.log("project saved");
        console.log(this.projectState);

        
    }

    static async Build(){
        await this.SaveProject();
        this.start_time = Date.now();

        this.build_directory = await FileManager.GetDirectory("output", await FileManager.GetDirectory("build"));

        this.setup_json = await this.#JSON();
        this.cc_js = await this.#CustomComponents();
        this.init = this.#GetInit();

        await this.#OutputBuild();
    }

    static async #JSON() {
        const seen = new WeakSet();

        const replacer = (key, value) => {
            if (typeof value === "object" && value !== null) {
                if (seen.has(value)) {
                    console.warn(`Circular reference detected at key: "${key}"`);
                    return "[Circular]"; 
                }
                seen.add(value);
            }
            return value;
        };

        return JSON.stringify(this.projectState, replacer, 2);
    }

    static async #CustomComponents(){
        
        let full_cc = "";
        let assets = await FileManager.GetDirectory("assets");
        
        let Directory = await FileManager.GetDirectory("components", assets);
        let components = await FileManager.GetFiles(Directory);
        let cc = "";
        
        for(let i = 0; i < components.length; i++){
            cc = await components[i].text();
            full_cc += "\n" + cc;
            cc = "";
        }

        return full_cc;
    }

    static async #GenerateHTML(){
        return `<!DOCTYPE html><html><head><title>${this.project_name}</title><style rel="stylesheet" href="deps/s.css"></style><script src='deps/init.js'></script></head><body><div class="parent"><canvas id="canvas"></canvas></div></body> </html>`;
    }

    static async #OutputBuild(){
        console.log("outputing build..");
        let deps_dir = await FileManager.MakeFolder("deps", this.build_directory);
        console.log("made deps/");

        // Engine boilerplate
        await FileManager.MakeFile("init.js", this.init, deps_dir);
        console.log("made deps/init.js");
        await FileManager.MakeFile("engine.js", await this.#CombineEngineFiles(), deps_dir);
        console.log("made deps/engine.js");
        await FileManager.MakeFile("physics.js", await this.#GetPhysics(), deps_dir);
        console.log("made deps/physics.js");

        // Project Settings
        await FileManager.MakeFile("loader.js", await this.#GetLoader(), deps_dir);
        console.log("made deps/loader.js");
        await FileManager.MakeFile("cc.js", this.cc_js, deps_dir);
        console.log("made deps/cc.js");

        // CSS
        await FileManager.MakeFile("s.css", await this.#GetCSS(), deps_dir);
        console.log("made deps/s.css");

        await FileManager.MakeFile(`${this.project_name}.html`, await this.#GenerateHTML(), this.build_directory);
        console.log(`made deps/${this.project_name}.html`);
        let end_time = Date.now()
        console.log(`Build Complete in:\n${(end_time - this.start_time) / 1000}s | ${end_time - this.start_time}ms`);
    }

    static async #CombineEngineFiles(){
        let engine_dir = await FileManager.GetDirectory("engine");

        let files = await FileManager.GetFiles(engine_dir);

        let ignore = ["matter", "setup", "event.js", "loader.js", "cc-loader.js"];

        let engine_combined = 
`
// Generated by builder.js.
// All Engine files are combined into this file below.
`;      


        // Order:
        // Math
        // Engine
        // GameObject
        // COmponents
        // Input
        

        for(let i = 0; i < files.length; i++){
            if(!ignore.includes(files[i].name)){
                if(files[i].name == "math.js"){
                    let file_code = await files[i].text();
                    engine_combined += "\n" + file_code;
                }
            }
        }
        for(let i = 0; i < files.length; i++){
            if(!ignore.includes(files[i].name)){
                if(files[i].name == "gameobject.js"){
                    let file_code = await files[i].text();
                    engine_combined += "\n" + file_code;
                }
            }
        }
         for(let i = 0; i < files.length; i++){
            if(!ignore.includes(files[i].name)){
                if(files[i].name == "components.js"){
                    let file_code = await files[i].text();
                    engine_combined += "\n" + file_code;
                }
            }
        }
         for(let i = 0; i < files.length; i++){
            if(!ignore.includes(files[i].name)){
                if(files[i].name == "input.js"){
                    let file_code = await files[i].text();
                    engine_combined += "\n" + file_code;
                }
            }
        }
        for(let i = 0; i < files.length; i++){
            if(!ignore.includes(files[i].name)){
                if(files[i].name == "engine.js"){
                    let file_code = await files[i].text();
                    engine_combined += "\n" + file_code;
                }
            }
        }

        return engine_combined;
    }

    static async #GetPhysics(){
        let matter = await FileManager.GetFile("matter.min.js", await FileManager.GetDirectory("matter", await FileManager.GetDirectory("engine")));
        let physics_js = await matter.text();
        return physics_js;
    }

    static async #GetCSS(){
        return `
        .parent{
              display: flex;
              justify-content: center;
              align-items: center;    
              height: 100vh;
        }
        `;
    }

    static async #GetLoader(){
        return`
        let _json = \`${this.setup_json}\`;
        
let obj = JSON.parse(_json);
for (const goData of obj.gameobjects) {
    let go = new GameObject(goData.name);
    go.transform.position.x = goData.transform.position.x;
    go.transform.position.y = goData.transform.position.y;
    go.transform.scale.x = goData.transform.scale.x;
    go.transform.scale.y = goData.transform.scale.y;

    go.renderer.type = goData.renderer.type
    go.renderer.display = goData.renderer.display
    go.renderer.color = new Color(goData.renderer.color.r, goData.renderer.color.g, goData.renderer.color.b, goData.renderer.color.a);

    for (const compData of goData.components) {
        let componentInstance = new window[compData.type](); 
        Object.assign(componentInstance, compData.data);
        go.AddComponent(componentInstance);
    }
}
Engine.SetTargetFPS(obj.target_fps);
Engine.fullscreen = obj.fullscreen;
Engine.DisplaySize.x = obj.display_size.x;
Engine.DisplaySize.y = obj.display_size.y;
Engine.Start('canvas');
        `;
    }

    static #GetInit(){
        return `async function async_main(){
    const sleep = (ms) => new Promise(resolve => setTimeout(resolve, ms));
    const scripts = [
        'deps/physics.js',
        'deps/engine.js',
        'deps/cc.js',
        'deps/loader.js'
    ];
    for(script of scripts){
        const ref = document.createElement('script');
        ref.src = script;
        ref.defer = true;
        document.head.appendChild(ref);
        console.log("Injected script: " + script);
        await sleep(75);
    };
    window.dispatchEvent(new Event('engine-loaded'));
}
async_main();



`;
    }
}


window.Builder = Builder;

